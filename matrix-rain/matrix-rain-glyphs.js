export function atlasFieldModeToValue(fieldMode) {
  switch (fieldMode) {
    case ATLAS_FIELD_MODES.mtsdf: return 2.0;
    case ATLAS_FIELD_MODES.msdf:  return 1.0;
    case ATLAS_FIELD_MODES.compat:
    default:
      return 0.0;
  }
}

export function resolveAtlasDescriptor({
  charSet = 'matrixcode',
  atlasPath = null,
  atlasGlyphCount = null,
  atlasGridW = null,
  atlasGridH = null,
  atlasFieldMode = null,
  atlasPxRange = null,
  atlasWidth = null,
  atlasHeight = null,
} = {}) {
  if (atlasPath !== null) {
    // Explicit atlasPath stays on a documented compatibility fallback unless the
    // caller also supplies decode metadata. This avoids guessing field mode from
    // file names or previous charset state.
    return {
      path: atlasPath,
      glyphCount: atlasGlyphCount ?? GLYPHS.length,
      gridW: atlasGridW ?? DEFAULT_COMPAT_ATLAS_GRID_W,
      gridH: atlasGridH ?? DEFAULT_COMPAT_ATLAS_GRID_H,
      fieldMode: atlasFieldMode ?? ATLAS_FIELD_MODES.compat,
      pxRange: atlasPxRange ?? DEFAULT_ATLAS_PX_RANGE,
      atlasWidth: atlasWidth ?? DEFAULT_COMPAT_ATLAS_WIDTH,
      atlasHeight: atlasHeight ?? DEFAULT_COMPAT_ATLAS_HEIGHT,
    };
  }

  const base = CHAR_SETS[charSet] ?? CHAR_SETS.matrixcode;
  return {
    ...base,
    fieldMode: atlasFieldMode ?? base.fieldMode,
    pxRange: atlasPxRange ?? base.pxRange,
    atlasWidth: atlasWidth ?? base.atlasWidth,
    atlasHeight: atlasHeight ?? base.atlasHeight,
  };
}

