/**
 * Build a 256-sample inverse-CDF glyph weight LUT as a DataTexture.
 * @param {number[]|null} weights  per-glyph relative weights, or null for uniform
 * @param {number}        glyphCount
 * @returns {THREE.DataTexture}
 */
export function buildGlyphWeightLUT(weights, glyphCount) {
  const lut = new Uint8Array(256);
  if (!weights) {
    for (let i = 0; i < 256; i++) lut[i] = Math.floor(i * glyphCount / 256);
  } else {
    const total = weights.reduce((a, b) => a + b, 0);
    const pdf   = weights.map(w => w / total);
    let cumul = 0, glyphIdx = 0;
    for (let i = 0; i < 256; i++) {
      const target = (i + 0.5) / 256;
      while (glyphIdx < glyphCount - 1 && cumul + pdf[glyphIdx] < target) {
        cumul += pdf[glyphIdx++];
      }
      lut[i] = glyphIdx;
    }
  }
  const tex = new THREE.DataTexture(lut, 256, 1, THREE.RedFormat, THREE.UnsignedByteType);
  tex.minFilter = tex.magFilter = THREE.NearestFilter;
  tex.needsUpdate = true;
  return tex;
}

/**
 * Upload the correct weight LUT for charSet into the uniforms node.
 * @param {string} charSet    key from CHAR_SETS (or any string; unknown keys get uniform LUT)
 * @param {number} glyphCount resolved glyph count for this atlas
 * @param {object} uniforms   result of makeUniforms()
 */
export function applyGlyphWeightLUT(charSet, glyphCount, uniforms) {
  const w = GLYPH_WEIGHTS[charSet] ?? null;
  uniforms.uGlyphWeightLUT.value = buildGlyphWeightLUT(w, glyphCount);
}

/**
 * Map a character to its glyph atlas index for the given charSet.
 * Returns -1 if the character has no mapping in this charset.
 */
export function charToGlyphIdx(char, charSet) {
  const c    = char.toUpperCase();
  const code = c.charCodeAt(0);
  switch (charSet) {
    case 'ascii':
    case 'iosevka':
    case 'gsanscode':
      // Printable ASCII: space(32) → index 0, through '~'(126) → index 94
      if (code >= 32 && code <= 126) return code - 32;
      return -1;
    case 'latin':
    case 'datatype':
    case 'orbitron':
      // A–Z → 0–25, 0–9 → 26–35
      if (code >= 65 && code <= 90) return code - 65;
      if (code >= 48 && code <= 57) return code - 48 + 26;
      return -1;
    case 'hebrew': {
      // Atlas layout: 22 base letters (alef–tav) at 0–21, 5 final forms at 22–26,
      // '$' at 27, '✡' at 28.  Base letters are non-contiguous in Unicode so use
      // indexOf rather than arithmetic.
      const HEBREW_BASE  = 'אבגדהוזחטיכלמנסעפצקרשת'; // U+05D0 – U+05EA (22 letters)
      const HEBREW_FINAL = 'ךםןףץ';                    // kaf/mem/nun/pe/tsadi sofit
      const bi = HEBREW_BASE.indexOf(char);
      if (bi >= 0) return bi;
      const fi = HEBREW_FINAL.indexOf(char);
      if (fi >= 0) return 22 + fi;
      if (char === '$')  return 27;
      if (char === '✡') return 28;
      return -1;
    }
    case 'arabic': {
      const ARABIC_BASE   = 'ابتثجحخدذرزسشصضطظعغفقكلمنهوي';
      const ARABIC_DIGITS = '٠١٢٣٤٥٦٧٨٩';
      const bi = ARABIC_BASE.indexOf(char);
      if (bi >= 0) return bi;
      const di = ARABIC_DIGITS.indexOf(char);
      if (di >= 0) return 28 + di;
      if (code >= 48 && code <= 57) return 28 + (code - 48);
      return -1;
    }
    case 'matrixcode':
    case 'matrix1999':
    case 'japanese':
    case 'chinese':
    case 'cyrillic':
    default:
      return -1;
  }
}
